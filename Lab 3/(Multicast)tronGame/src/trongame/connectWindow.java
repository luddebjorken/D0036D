    void search(){
        try {
            int port = 1900;
            InetAddress group = InetAddress.getByName("239.255.255.250");
            MulticastSocket broadcastSocket = new MulticastSocket();
            byte[] buffer = "SERVICE REQUEST tronJavaGame".getBytes();
            DatagramPacket packet = new DatagramPacket(buffer, buffer.length, group, port);
            broadcastSocket.send(packet);
            if(searchThread!=null) searchThread.running = false;
            searchThread = new searchResponseThread(this);
            searchThread.start();
            System.out.println("Listener started!");
            
        } catch (UnknownHostException ex) {
            Logger.getLogger(connectWindow.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(connectWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    void join() throws UnknownHostException, IOException{
        int index = serverSelectorBox.getSelectedIndex();
        System.out.println("Connecting to: " + serverList[index].ip + ":" + serverList[index].port);
        server = new Socket(serverList[index].ip, serverList[index].port);
        out = new DataOutputStream(server.getOutputStream());
        in = new DataInputStream(server.getInputStream());
        
        out.writeUTF("0|" + nameField.getText());
        out.flush();
        
        Thread waitThread = new joiningThread(this);
        waitThread.start();
    }
    
    void startRequest() throws IOException{
        out.writeUTF("1");
    }
    
