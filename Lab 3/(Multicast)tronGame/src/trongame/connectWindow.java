class serverObject{
    String name;
    InetAddress ip;
    int port;
    public serverObject(String name, String ip, String port){
        try {
            if(ip.contains("/")) ip = ip.substring(ip.indexOf("/")+1); //Failsafe
            this.name = name;
            this.ip = InetAddress.getByName(ip);
            this.port = Integer.parseInt(port);
        } catch (UnknownHostException ex) {
            Logger.getLogger(serverObject.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
}
